function restart() {
    // إخفاء كل الأقسام
    document.getElementById('resultSection').classList.add('hidden');
    document.getElementById('againSection').classList.add('hidden');
    document.getElementById('thankYouSection').classList.add('hidden');
    
    // مسح حقل الإدخال
    document.getElementById('elementInput').value = '';
    
    // إعادة التركيز لحقل الإدخال
    document.getElementById('elementInput').focus();
    
    // إعادة تفعيل زر البحث لو كان معطل
    document.querySelector('button[onclick="searchElement()"]').disabled = false;
}

function stop() {
    // إخفاء الأقسام غير المرغوب فيها
    document.getElementById('resultSection').classList.add('hidden');
    document.getElementById('againSection').classList.add('hidden');
    
    // إظهار رسالة الشكر
    document.getElementById('thankYouSection').classList.remove('hidden');
    
    // تعطيل البحث
    document.querySelector('button[onclick="searchElement()"]').disabled = true;
}

// إضافة خاصية البحث بالضغط على Enter
