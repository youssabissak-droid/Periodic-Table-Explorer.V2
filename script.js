// دوال البرنامج الرئيسية
function searchElement() {
    const input = document.getElementById('elementInput');
    let symbol = input.value.trim();

    if (symbol === '') {
        alert('Please enter an element symbol');
        return;
    }

    // تنسيق الرمز: أول حرف كبير والباقي صغير
    symbol = symbol.charAt(0).toUpperCase() + symbol.slice(1).toLowerCase();

    const element = elements[symbol];

    // إخفاء الأقسام القديمة
    document.getElementById('resultSection').classList.add('hidden');
    document.getElementById('againSection').classList.add('hidden');
    document.getElementById('thankYouSection').classList.add('hidden');

    if (element) {
        displayElementInfo(element, symbol);
        document.getElementById('resultSection').classList.remove('hidden');
        document.getElementById('againSection').classList.remove('hidden');
    } else {
        displayNotFound(symbol);
        document.getElementById('resultSection').classList.remove('hidden');
        document.getElementById('againSection').classList.remove('hidden');
    }
}

